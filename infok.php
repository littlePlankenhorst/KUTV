            <h2>A versenyről</h2>
            <p>A Babeş-Bolyai Tudományegyetem Közgazdaság- és Gazdálkodástudományi Magyar Intézetének szervezésében, 2024. április 19-21. között, tizenhatodik alkalommal kerül megrendezésre a Kolozsvári Esettanulmány Napok rendezvénysorozat. Az Esettanulmány Napok keretén belül kiemelt helyet kap a már komoly hagyományokkal rendelkező Középiskolások Üzleti Tanácsadó Versenyének döntő fordulója.</p>

            <p>A Középiskolások Üzleti Tanácsadó Versenye magyar anyanyelvű középiskolás diákoknak szervezett gazdasági verseny. Célunk a közgazdász szakma rejtélyeibe betekintést nyújtani a pályaválasztás előtt álló fiatalok számára. Ennek érdekében egyszerű, gyakorlatias és szórakoztató tanulási módszerek segítségével építettük fel a Középiskolások Üzleti Tanácsadó Versenyét. A verseny alatt a diákok megismerkedhetnek a piackutatás területével, majd az itt szerzett ismereteket egy esettanulmány megoldása közben hasznosíthatják.</p>

            <h3>A verseny menete</h3>
            <p>Az előző évekhez hasonlóan a hagyományosan Kolozsváron megszervezett verseny képezi a döntő szakaszt, amelyet egy online forduló előz meg. A verseny első fordulóját egy piackutatási (egy kérdőívkitöltetésből álló kizáró jellegű és egy, ezzel nem feltétlenül szorosan összefüggő iparági áttekintési) probléma képezi, a döntő forduló pedig egy összetettebb vállalatvezetői probléma megoldását feltételezi. A forduló feladatát úgy építettük fel, hogy felkészítsék a diákokat a döntőben sorra kerülő eset megoldására.</p>

            <h3>Jelentkezés</h3>
            <p>A versenyre 3 fős középiskolás diákokból álló csapatok jelentkezhetnek az alábbi linken elérhető regisztrációs űrlap kitöltésével: <a href="#">REGISZTRÁCIÓ</a>.</p>

            <h3>Döntő</h3>
            <p>A kolozsvári élő (offline) döntőben az online forduló alapján legjobban teljesítő, egyenként 3 fős középiskolai csapatok vesznek részt. Egy „zöld kártyás", szabad helyet biztosítunk a döntőben annak a csapatnak, amely iskolájából egy csapat sem érte el ugyan a továbbjutást, de az adott csapat a legjobban teljesített az online fordulóban azon nem továbbjutó iskolák csapatai közül, amelyek a tavaly nem vettek részt a kolozsvári döntőben. A döntőben dobogós helyezést elérő csapatok tagjai, a díjazás mellett, a Közgazdaság- és Gazdálkodástudományi Karra történő felvételi esetén plusz pontokat kapnak az érettségi átlagukhoz.</p>

            <p>A Középiskolások Üzleti Tanácsadó Versenyének döntőjével párhuzamosan zajlik a Kolozsvári Egyetemista Esettanulmány Verseny, melynek döntőjét a középiskolás diákok nézőként követhetik.</p>

            <h3>A versenyfordulók</h3>
            <h4>1. Piackutatás : adatgyűjtés és iparágelemzés – online – szelekció</h4>
            <p>A jelentkező csapatoknak a feladata egy piackutatás elvégzése megadott mintavételi és módszertani kritériumok figyelembevételével. Ez a feladat a kérdőívek minimum számát tekintve kizáró jellegű.</p>
            <p>A második feladat, hogy a csapatoknak egy másfél oldalas, hiteles forrásokkal alátámasztott elemzést kell készíteniük egy adott témában (nem kötelezően a kérdőívek felhasználásával).</p>
            <p>Az első forduló részletes leírását és az elemzés szempontjait a verseny ütemtervében megjelölt időpontban kiküldjük a csapatoknak.</p>

            <h4>2. Esettanulmány döntő – Kolozsvár, Babeş-Bolyai Tudományegyetem, Közgazdaság- és Gazdálkodástudományi Kar, személyes jelenléttel</h4>
            <p>Az első forduló során továbbjutott csapatok a döntőben mérik össze tudásukat és készségeiket egy valós vállalati esettanulmány megoldása és bemutatása során. Az esettanulmányt a döntőbe jutott csapatok több mint egy héttel a döntő forduló előtt megkapják, viszont az nem fogja tartalmazni a feladatot. A konkrétan megoldandó feladatot a döntős csapatok a döntő szombati napján kapják meg. A Kolozsváron, a Babeş-Bolyai Tudományegyetem Közgazdaság- és Gazdálkodástudományi Karának székhelyén zajló döntő első napján (szombaton) a csapatok egymástól és a külvilágtól elszigetelten (internet és egyéb kommunikációs eszközök használata nélkül) dolgozzák ki megoldásaikat, amelyeket a verseny második napján (vasárnap) gyakorló szakemberekből álló zsűri előtt mutatnak be.</p>

            <h3>Háttérinformációk</h3>
            <p>A piackutatás segítségével a vállalatvezetők információt gyűjtenek a vevőkről, a versenytársakról és más piaci szereplőkről. Az összegyűjtött információkra alapozva hozzák meg döntéseiket. A sikeres vállalat kiinduló pontja a piaci adottságok és a vevők szükségleteinek a megértése és azok kielégítése a versenytársak ajánlatánál kedvezőbb feltételek mellett.</p>
            <p>Az esettanulmány valós információk alapján mutat be egy vállalati problémát. Az esettanulmány tartalmazza az adott vállalat történetét, gazdasági környezetét, jelenlegi helyzetét, valamint jövőbeli kilátásait. Az esettanulmány versenyen résztvevő csapatok feladata, hogy a megkapott esettanulmányok elolvasása, tanulmányozása révén felismerjék, megfogalmazzák a vállalat problémáit, illetve azonosítsák a vállalat jövőbeni fejlődésének lehetséges irányait.</p>

